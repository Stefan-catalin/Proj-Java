package com.credite;

public class rate {
    private float rataLunara;
    private int nrLuni;
    rate(){
        rataLunara = 0;
        nrLuni = 0;
    }
    public float getRataLunara() {
        return rataLunara;
    }
    public int getNrLuni() {
        return nrLuni;
    }

    public void setRataLunara(float r) {
        rataLunara = r;
    }
    public void setNrLuni(int r) {
        nrLuni = r;
    }
}
